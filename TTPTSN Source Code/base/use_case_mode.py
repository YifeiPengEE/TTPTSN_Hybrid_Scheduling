class GeneTopo:
    @staticmethod
    def get_pm(col, row, interval):
        sw_num = row * col
        es_num = row * col
        node_num = sw_num + es_num
        adj_matrix_pm = np.zeros((node_num, node_num))
        # 把每一列串起来
        for r in range(0, row - 1):
            for c in range(0, col):
                adj_matrix_pm[r * col + c][(r + 1) * col + c] = 1
                adj_matrix_pm[(r + 1) * col + c][r * col + c] = 1

        # 把每隔interval行的行串起来
        for r in range(0, row):
            if r % interval == 0:
                for c in range(0, col - 1):
                    adj_matrix_pm[r * col + c][r * col + c + 1] = 1
                    adj_matrix_pm[r * col + c + 1][r * col + c] = 1
        # 把节点和交换机相连
        for r in range(0, row):
            for c in range(0, col):
                adj_matrix_pm[r * col + c][r * col + c + sw_num] = 1
                adj_matrix_pm[r * col + c + sw_num][r * col + c] = 1
