class randomGraph():
    def __init__(self):
        print('randomGraph')

    def generate_network(self,num_end_systems, num_switches):
        num_nodes = num_end_systems + num_switches
        adjacency_matrix = [[0 for _ in range(num_nodes)] for _ in range(num_nodes)]

        # 连接所有交换机，确保交换机子图连通
        for i in range(1, num_switches):
            adjacency_matrix[i - 1][i] = 1
            adjacency_matrix[i][i - 1] = 1

        # 如果有多于一个交换机，可以随机连接交换机以增加连通性
        if num_switches > 2:
            # extra_switch_edges = num_switches - 1  # 额外增加的交换机连接数
            extra_switch_edges = num_switches/2
            while extra_switch_edges > 0:
                a, b = random.randint(0, num_switches - 1), random.randint(0, num_switches - 1)
                if a != b and adjacency_matrix[a][b] == 0:
                    adjacency_matrix[a][b] = 1
                    adjacency_matrix[b][a] = 1
                    extra_switch_edges -= 1

        # 将每个端系统随机连接到至少一个交换机
        for es in range(num_switches, num_nodes):
            switch = random.randint(0, num_switches - 1)
            adjacency_matrix[es][switch] = 1
            adjacency_matrix[switch][es] = 1

        # 可选：为端系统随机添加更多交换机连接
        for es in range(num_switches, num_nodes):
            if num_switches <= 1:
                break
            # additional_connections = random.randint(0, num_switches - 1)  # 每个端系统额外连接的交换机数量
            additional_connections = random.randint(0,1)
            connections = 0

            while connections < additional_connections:
                switch = random.randint(0, num_switches - 1)
                if adjacency_matrix[es][switch] == 0:
                    adjacency_matrix[es][switch] = 1
                    adjacency_matrix[switch][es] = 1
                    connections += 1

        return adjacency_matrix

